/**
 * build-area-centroids.mjs
 *
 * fix(audit cov-norfolk-centroid-collapse / coverage-vb-shared-centroid /
 * map-sacramento-single-centroid): some city feeds publish a neighborhood NAME
 * per incident but NO coordinates, so their adapters fell back to a single
 * citywide centroid for every area. That collapses "use my location" → nearest
 * area (every area is equidistant) and stacks every map dot on one point.
 *
 * We already ship the real area POLYGONS for these cities in
 * apps/web/public/geo/<city>.geojson (used to draw the choropleth). This tool
 * computes each polygon's geometric centroid and emits a generated
 * slug → {lat,lng} table the adapters use instead of the citywide point. The
 * centroids are REAL (derived from the official boundary polygons), never
 * fabricated. Network-free — reads only the bundled geojson.
 *
 * Slugging matches the adapters EXACTLY (`<prefix><slugify(name)>`), same as
 * tools/build-neighborhood-populations.mjs, so keys line up with discovery,
 * the population table, and the crime map's normName matching.
 *
 * Run:  node tools/build-area-centroids.mjs
 */
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const GEO_DIR = path.join(REPO_ROOT, "apps/web/public/geo");
const OUTPUT_PATH = path.join(REPO_ROOT, "packages/crime-data/src/area-centroids-generated.ts");

// Coordless-feed cities whose adapters collapse to a single citywide centroid.
// slugPrefix must match the adapter's slug construction.
const CITY_CONFIG = {
  "norfolk":        { slugPrefix: "nor-" },
  "virginia-beach": { slugPrefix: "vb-" },
  "sacramento":     { slugPrefix: "sac-" },
};

function slugify(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

// Area-weighted polygon centroid (planar shoelace on lng/lat). Falls back to the
// vertex average for degenerate (zero-area) rings. Good to ~5 decimal places at
// neighborhood scale.
function ringCentroid(ring) {
  let a = 0, cx = 0, cy = 0;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const [xi, yi] = ring[i];
    const [xj, yj] = ring[j];
    const cross = xj * yi - xi * yj;
    a += cross;
    cx += (xj + xi) * cross;
    cy += (yj + yi) * cross;
  }
  a /= 2;
  if (Math.abs(a) < 1e-12) {
    // Degenerate ring — average the vertices instead.
    let sx = 0, sy = 0;
    for (const [x, y] of ring) { sx += x; sy += y; }
    return ring.length ? { lng: sx / ring.length, lat: sy / ring.length, area: 0 } : null;
  }
  return { lng: cx / (6 * a), lat: cy / (6 * a), area: Math.abs(a) };
}

// Centroid of a Feature: the area-weighted mean of its outer rings (largest ring
// dominates for MultiPolygons, which is what we want — the main landmass).
function featureCentroid(feature) {
  const g = feature.geometry;
  if (!g) return null;
  const polys = g.type === "Polygon" ? [g.coordinates] : g.type === "MultiPolygon" ? g.coordinates : [];
  let sx = 0, sy = 0, sa = 0;
  for (const poly of polys) {
    if (!poly[0]) continue;
    const c = ringCentroid(poly[0]);
    if (!c) continue;
    const w = c.area || 1e-9;
    sx += c.lng * w; sy += c.lat * w; sa += w;
  }
  if (sa === 0) return null;
  return { lat: +(sy / sa).toFixed(5), lng: +(sx / sa).toFixed(5) };
}

async function main() {
  const all = {};
  for (const [citySlug, cfg] of Object.entries(CITY_CONFIG)) {
    const geoPath = path.join(GEO_DIR, `${citySlug}.geojson`);
    let geo;
    try {
      geo = JSON.parse(await fs.readFile(geoPath, "utf-8"));
    } catch {
      console.log(`${citySlug.padEnd(16)} SKIP (no geojson)`);
      continue;
    }
    const table = {};
    for (const feat of geo.features ?? []) {
      const name = feat.properties?.name;
      if (!name) continue;
      const c = featureCentroid(feat);
      if (!c) continue;
      table[cfg.slugPrefix + slugify(name)] = c;
    }
    all[citySlug] = table;
    console.log(`${citySlug.padEnd(16)} ${Object.keys(table).length} centroids`);
  }

  const lines = [
    "// AUTO-GENERATED by tools/build-area-centroids.mjs — DO NOT EDIT BY HAND.",
    "// Real per-area centroids derived from the official boundary polygons in",
    "// apps/web/public/geo/<city>.geojson, for cities whose incident feed has no",
    "// coordinates (so their adapter would otherwise collapse every area onto one",
    "// citywide point). Re-run the tool after a geojson changes.",
    "export interface LatLng { lat: number; lng: number }",
    "export const GENERATED_AREA_CENTROIDS: Record<string, Record<string, LatLng>> = {",
  ];
  for (const city of Object.keys(all).sort()) {
    lines.push(`  ${JSON.stringify(city)}: {`);
    for (const slug of Object.keys(all[city]).sort()) {
      const c = all[city][slug];
      lines.push(`    ${JSON.stringify(slug)}: { lat: ${c.lat}, lng: ${c.lng} },`);
    }
    lines.push(`  },`);
  }
  lines.push("};", "");
  await fs.writeFile(OUTPUT_PATH, lines.join("\n"));
  console.log(`\nWrote ${OUTPUT_PATH}`);
}

main();
